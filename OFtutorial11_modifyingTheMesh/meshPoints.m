a=[0.0, 0, 0;
0.5, 0, 0;
1.0, 0, 0;
0.0, 1, 0;
0.5, 1, 0;
1.0, 1, 0;
0.0, 0, 0.1;
0.5, 0, 0.1;
1.0, 0, 0.1;
0.0, 1, 0.1;
0.5, 1, 0.1;
1.0, 1, 0.1;
0.7, 0.5, -0.5;
0.7, 1.0, -0.5;
0.7, 1.2, -0.5;
0.5, 1.2, 0.0;
1.0, 1.2, 0.0];
x=a(:,1);
y=a(:,2);
z=a(:,3);
plot3(x,y,z,'o')
xlabel('x')
ylabel('y')
zlabel('z')
grid on;
for i=1:16%用这个循环
    c=num2str(i-1);
    c=[' ',c];
    text(x(i),y(i),z(i),c)
end
set(gca,'XDir','reverse')%对x方向反转
set(gca,'ZDir','reverse')%对z方向反转